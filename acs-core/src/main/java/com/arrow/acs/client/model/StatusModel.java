package com.arrow.acs.client.model;

import java.io.Serializable;

public class StatusModel implements Serializable {
	private static final long serialVersionUID = -8506135907345705410L;

	public final static StatusModel OK = new StatusModel().withStatus("OK").withMessage("");

	public static StatusModel error(String error) {
		return new StatusModel().withStatus("ERROR").withMessage(error);
	}

	private String status;
	private String message;

	public StatusModel withStatus(String status) {
		setStatus(status);
		return this;
	}

	public StatusModel withMessage(String message) {
		setMessage(message);
		return this;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((message == null) ? 0 : message.hashCode());
		result = prime * result + ((status == null) ? 0 : status.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		StatusModel other = (StatusModel) obj;
		if (message == null) {
			if (other.message != null)
				return false;
		} else if (!message.equals(other.message))
			return false;
		if (status == null) {
			if (other.status != null)
				return false;
		} else if (!status.equals(other.status))
			return false;
		return true;
	}
}
