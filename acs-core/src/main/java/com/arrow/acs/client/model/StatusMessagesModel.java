package com.arrow.acs.client.model;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

public class StatusMessagesModel implements Serializable {
	private static final long serialVersionUID = -3750398194559657894L;

	public final static StatusMessagesModel OK = new StatusMessagesModel().withStatus("OK");

	public static StatusMessagesModel error(List<String> messages) {
		return new StatusMessagesModel().withStatus("ERROR").withMessages(messages);
	}

	private String status;
	private List<String> messages = new ArrayList<>();

	public StatusMessagesModel withStatus(String status) {
		setStatus(status);
		return this;
	}

	public StatusMessagesModel withMessages(List<String> messages) {
		setMessages(messages);
		return this;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public List<String> getMessages() {
		return messages;
	}

	public void setMessages(List<String> messages) {
		this.messages = messages;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((messages == null) ? 0 : messages.hashCode());
		result = prime * result + ((status == null) ? 0 : status.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		StatusMessagesModel other = (StatusMessagesModel) obj;
		if (messages == null) {
			if (other.messages != null)
				return false;
		} else if (!messages.equals(other.messages))
			return false;
		if (status == null) {
			if (other.status != null)
				return false;
		} else if (!status.equals(other.status))
			return false;
		return true;
	}
}
