package com.arrow.acn.client.model;

import java.io.Serializable;

import com.arrow.acs.AcsUtils;

public class AccountRegistrationModel implements Serializable {
	private static final long serialVersionUID = -3509111120620111818L;

	private String name;
	private String email;
	private String password;
	private String code;

	public void trim() {
		name = AcsUtils.trimToNull(name);
		email = AcsUtils.trimToNull(email);
		password = AcsUtils.trimToNull(password);
		code = AcsUtils.trimToNull(code);
	}

	public AccountRegistrationModel withName(String name) {
		setName(name);
		return this;
	}

	public AccountRegistrationModel withEmail(String email) {
		setEmail(email);
		return this;
	}

	public AccountRegistrationModel withPassword(String password) {
		setPassword(password);
		return this;
	}

	public AccountRegistrationModel withCode(String code) {
		setCode(code);
		return this;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((code == null) ? 0 : code.hashCode());
		result = prime * result + ((email == null) ? 0 : email.hashCode());
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		result = prime * result + ((password == null) ? 0 : password.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		AccountRegistrationModel other = (AccountRegistrationModel) obj;
		if (code == null) {
			if (other.code != null)
				return false;
		} else if (!code.equals(other.code))
			return false;
		if (email == null) {
			if (other.email != null)
				return false;
		} else if (!email.equals(other.email))
			return false;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		if (password == null) {
			if (other.password != null)
				return false;
		} else if (!password.equals(other.password))
			return false;
		return true;
	}
}
