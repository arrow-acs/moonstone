package com.arrow.acn.client.model;

import java.io.Serializable;

public class RegisterSocialEventRegistrationModel implements Serializable {
	private static final long serialVersionUID = 2652754792835097956L;

	private String name;
	private String email;
	private String password;
	private String eventCode;
	private String socialEventHid;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getEventCode() {
		return eventCode;
	}

	public void setEventCode(String eventCode) {
		this.eventCode = eventCode;
	}

	public String getSocialEventHid() {
		return socialEventHid;
	}

	public void setSocialEventHid(String socialEventHid) {
		this.socialEventHid = socialEventHid;
	}
}
