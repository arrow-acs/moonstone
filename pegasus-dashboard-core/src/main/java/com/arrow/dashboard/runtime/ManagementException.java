package com.arrow.dashboard.runtime;

public class ManagementException extends Exception {

	private static final long serialVersionUID = -4053964197278922337L;

	public ManagementException() {
		super();
	}

	public ManagementException(String arg0) {
		super(arg0);
	}

	public ManagementException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ManagementException(Throwable arg0) {
		super(arg0);
	}

}
